import Image, ImageFont, ImageDraw


def list2byte(ArrayOfBinaryInts):
    byte  = 0
    for i in range(8):
        byte += 2**(7-i) if ArrayOfBinaryInts[i] else 0
    return byte


def generateImageArrayFromText(s, offset, img_size):
    image = Image.new("RGBA", img_size, (0,0,0))
    draw = ImageDraw.Draw(image)
    draw.fontmode = "1"         # No AA
    font = ImageFont.load_default()
    
    draw.text(offset, s, font=font, fill=(255,255,255))

    imgmap = []
    for pixel in image.getdata():
        r, g, b, a = pixel
        if r == 255:
            imgmap.append(1)
        else:
            imgmap.append(0)
    return imgmap

